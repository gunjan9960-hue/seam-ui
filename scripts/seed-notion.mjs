async function main() {
  console.log("Creating parent page 'Seam Test Data — BazaarVoice Synthetic'...");

  // Use PARENT_PAGE_ID env var, or fall back to search
  let rootPageId = process.env.PARENT_PAGE_ID?.replace(/-/g, "");
  if (!rootPageId) {
    const search = await notionPost("/search", { query: "", filter: { value: "page", property: "object" }, page_size: 1 });
    rootPageId = search.results?.[0]?.id;
  }

  if (!rootPageId) {
    console.error("No accessible page found. Set PARENT_PAGE_ID=<notion-page-id> or share a page with this integration.");
    process.exit(1);
  }

  let parentId;
  // Create a container page
  const container = await notionPost("/pages", {
    parent: { page_id: rootPageId },
    properties: {
      title: { title: [{ text: { content: "Seam Test Data — BazaarVoice Synthetic" } }] },
    },
    children: [p("20 synthetic PM documents for Seam demo. Generated June 2026.")],
  });
  parentId = container.id;
  console.log(`Container page created: ${container.id}`);

  for (let i = 0; i < DOCS.length; i++) {
    const doc = DOCS[i];
    const pageBody = {
      properties: {
        title: { title: [{ text: { content: doc.title } }] },
      },
      children: doc.blocks,
    };

    pageBody.parent = { page_id: parentId };

    try {
      const page = await notionPost("/pages", pageBody);
      console.log(`[${i + 1}/20] Created: ${doc.title}`);
    } catch (err) {
      console.error(`[${i + 1}/20] FAILED: ${doc.title} — ${err.message}`);
    }

    // Small delay to avoid rate limiting
    await new Promise(r => setTimeout(r, 300));
  }

  console.log("\nDone. Trigger a Notion sync in Seam to index these documents.");
}

main().catch(console.error);
